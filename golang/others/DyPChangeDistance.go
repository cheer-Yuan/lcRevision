func numDistinct(s string, t string) int {
	lenS, lenT := len(s), len(t)
	dp := make([][]int, lenT + 1)
	for i := 0; i <= lenT; i++ {
		dp[i] = make([]int, lenS + 1)
	}

	//åˆå§‹åŒ–
	for j := 0; j <= lenS; j++ {
		dp[0][j] = 1
	}

	for i := 1; i <= lenT; i++ {
		for j := i; j <= lenS; j++ {
			if s[j - 1] != t[i - 1] {
				dp[i][j] = dp[i][j - 1]
			} else {
				dp[i][j] = dp[i][j - 1] + dp[i - 1][j - 1]
			}
		}
	}

	return dp[lenT][lenS]
}

